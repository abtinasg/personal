-- ============================================================
--  مهاجرت ۰۱۵ — نقشِ کاربر (RBAC) برای دسترسیِ تیمیِ پنلِ مدیریت
--
--  مشکل: تا الان «ادمین» با ENVِ ADMIN_USERNAME تعیین می‌شد — فقط یک نفر.
--  برای تیمِ چندنفره غلط است. این مهاجرت یک ستونِ role اضافه می‌کند تا
--  چند نفر با سطح‌دسترسیِ متفاوت ادمین باشند (کمترین‌دسترسی).
--
--  نقش‌ها (از کم به زیاد):
--    user    — کاربرِ عادی، بدونِ پنل.
--    support — فقط خواندن: داشبورد + تیکت‌ها. حذف/خروجی/کلیدِ قطع نه.
--    admin   — مدیریتِ داده و کاربر و کلیدهای قطع.
--    owner   — همه‌چیز + مدیریتِ نقشِ بقیه + خروجیِ کاملِ دیتا.
--
--  بوت‌استرپ: کاربری که username اش برابرِ ADMIN_USERNAME است، در اولین ورود
--  به پنل خودکار owner می‌شود (در کد)، تا قفل نشوید. بعد از آن می‌توانید بقیه
--  را از پنل ارتقا دهید.
--
--  افزایشی و بی‌خطر. روی Arvan/Postgres اجرا کن.
-- ============================================================

alter table public.users add column if not exists role text not null default 'user';

-- ایندکس برای پیدا کردنِ سریعِ اعضای تیم (نقش غیر از user).
create index if not exists users_role_idx on public.users(role) where role <> 'user';

