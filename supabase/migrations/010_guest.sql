-- ============================================================
--  مهاجرت ۰۱۰ — کاربرِ مهمان («اول بچش، بعد ثبت‌نام»)
--
--  کاربر می‌تواند بدونِ شماره وارد شود و اپ را بچشد. در این حالت یک
--  ردیفِ users با is_guest=true و بدونِ phone ساخته می‌شود. وقتی بعداً
--  شماره‌اش را می‌دهد، همان ردیف phone می‌گیرد و is_guest=false می‌شود؛
--  چون همه‌ی داده‌ها به user_id وصل‌اند، خودکار «منتقل» می‌شوند.
--  افزایشی و بی‌خطر. Supabase → SQL Editor → اجرا کن.
-- ============================================================

alter table public.users add column if not exists is_guest boolean not null default false;
