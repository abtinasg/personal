--  یک پرچمِ ساده تا بدانیم کاربر مراحلِ آشناییِ اولِ بعد از ورود را
--  دیده و مشخصاتِ پایه (اسم، قد، وزن، جنسیت، سن، فعالیت) را داده است.
--  افزایشی و بی‌خطر. Supabase → SQL Editor → اجرا کن.
