alter table public.profiles add column if not exists onboarded boolean not null default false;

-- کاربرانِ فعلی را «انبوردشده» در نظر بگیر تا مجبور به طیِ مراحل نشوند؛
-- فقط حساب‌هایی که از این به بعد ساخته می‌شوند با پیش‌فرضِ false وارد انبوردینگ می‌شوند.
update public.profiles set onboarded = true where onboarded = false;
